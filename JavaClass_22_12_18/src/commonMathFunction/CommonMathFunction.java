package commonMathFunction;

public class CommonMathFunction {

    public static void main(String[] args) {
        System.out.println(Math.sqrt(25));
        System.out.println(Math.pow(5, 5));
        System.out.println(Math.ceil(25.123456));
        System.out.println(Math.floor(25.123456));
        System.out.println(Math.rint(25.123456));
        System.out.println(Math.rint(25.49));
        System.out.println(Math.round(25.123456));
    }
}
